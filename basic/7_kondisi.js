/* 
 * if else 
 */
var nilaiLaki = 10;

if(nilaiLaki == 10){
    console.log('mendapat buah apple');
}else if(nilaiLaki == 5){
    console.log('mendapat buah cherry');
}else{
    console.log('tidak mendapat buah');
}

